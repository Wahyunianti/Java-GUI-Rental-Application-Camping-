    private void jLabel6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel6MouseClicked
        ClassValidasi cv = new ClassValidasi();
        username = txtUsername.getText();
        getDataUser(username);
        String level = cv.Login2(txtUsername.getText(), txtPassword.getText());
        if ("Admin".equals(level)) {
            new MenuAdmin(namalogin).setVisible(true);
            dispose();
        } else if ("Penyewa".equals(level)) {
            new MenuPenyewa(namalogin).setVisible(true);
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Account tidak terdaftar");
        }
    }//GEN-LAST:event_jLabel6MouseClicked

    private void jLabel9MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel9MouseClicked
        String nm = txtNama.getText();
        String usr = txtUname.getText();
        String pw = txtPw.getText();
        String pilih = null;
        if (rbAdmin.isSelected()) {
            pilih = "Admin";
        }
        if (rbPenyewa.isSelected()) {
            pilih = "Penyewa";
        }
        ClassUser cp = new ClassUser();
        cp.InsertUser(nm, usr, pw, pilih);

        jTabbedPane1.setSelectedIndex(0);
    }//GEN-LAST:event_jLabel9MouseClicked

