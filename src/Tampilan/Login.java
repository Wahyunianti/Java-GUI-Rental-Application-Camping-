    public void getDataUser(String key) {
        ClassUser cb = new ClassUser();
        cb.getDataUser(key);
        int ndata = cb.getNumberDataUser();
        Object[][] data = cb.getAllDataUser();
        Object[] data1 = new Object[4];
        for (int i = 0; i < ndata; i++) {
            for (int j = 0; j < 4; j++) {
                data1[j] = data[i][j];
                System.out.println(data1[j]);
            }
            namalogin = String.valueOf(data1[0]);
            System.out.println(namalogin);
        }
    }

